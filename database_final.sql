-- ==============================================================
-- БЛОК 11: RPC — ОДОБРЕНИЕ / ОТКЛОНЕНИЕ SC-ЗАЯВОК
--
-- Логика распределения баланса:
--
--   МФ_Бой / МФ_Тренировка:
--     Обе фракции ставят по fund SC.
--     Победитель получает +fund (выигрывает ставку соперника).
--     Проигравший теряет  -fund (теряет свою ставку).
--     Итог: winner_balance += fund,  loser_balance -= fund
--
--   Задание:
--     Фракция-исполнитель получает +task_reward SC от правительства.
--     Проигравших нет — только начисление.
--
--   Викторина:
--     Фракция-победитель получает +fund SC.
--
-- ==============================================================
DROP FUNCTION IF EXISTS public.update_proposal_status_secure(bigint, text, text, text, text);
DROP FUNCTION IF EXISTS public.update_proposal_status_secure(bigint, text, text, text, text, integer);
DROP FUNCTION IF EXISTS public.update_proposal_status_secure(bigint, text, text, text, text, integer, bigint);
DROP FUNCTION IF EXISTS public.update_proposal_status_secure(bigint, text, text, text, text, text, text, integer);
DROP FUNCTION IF EXISTS public.update_proposal_status_secure(int,    text, text, text, text, int);

CREATE OR REPLACE FUNCTION public.update_proposal_status_secure(
  p_proposal_id         bigint,
  p_new_status          text,
  p_comment             text,
  p_admin_login         text,
  p_admin_password_hash text,
  p_task_reward         integer DEFAULT NULL,  -- заполняется только для типа «Задание»
  p_winner_name         text    DEFAULT NULL,  -- заполняется только для типа «МФ_Бой»
  p_loser_name          text    DEFAULT NULL   -- заполняется только для типа «МФ_Бой»
)
RETURNS boolean AS $$
DECLARE
  v_proposal    public.sc_proposals%ROWTYPE;
  v_winner_id   bigint;
  v_loser_id    bigint;
  v_fund        bigint;
  v_faction_row public.factions%ROWTYPE;
BEGIN
  -- 1. Авторизация
  IF NOT EXISTS (
    SELECT 1 FROM public.admin_users
    WHERE username = p_admin_login AND password = p_admin_password_hash
  ) THEN
    RAISE EXCEPTION 'Доступ запрещён: неверные учётные данные.' USING ERRCODE = '42501';
  END IF;

  -- 2. Загружаем заявку
  SELECT * INTO v_proposal FROM public.sc_proposals WHERE id = p_proposal_id;
  IF NOT FOUND THEN
    RAISE EXCEPTION 'Заявка не найдена.' USING ERRCODE = 'P0002';
  END IF;

  -- 3. Если заявка уже обработана — защита от двойного нажатия
  IF v_proposal.status != 'На рассмотрении' THEN
    RAISE EXCEPTION 'Заявка уже была обработана ранее.';
  END IF;

  -- 4. Логика начисления (только при одобрении)
  IF p_new_status = 'Одобрено' THEN

    -- ── МФ_Бой / МФ_Тренировка ──────────────────────────────
    IF v_proposal.type IN ('МФ_Бой', 'МФ_Тренировка') THEN
      -- Оба имени обязательны
      IF p_winner_name IS NULL OR p_loser_name IS NULL THEN
        RAISE EXCEPTION 'Для типа «%» необходимо указать победителя и проигравшего.', v_proposal.type;
      END IF;

      -- Ищем фракции по имени
      SELECT id INTO v_winner_id FROM public.factions WHERE name = p_winner_name;
      SELECT id INTO v_loser_id  FROM public.factions WHERE name = p_loser_name;

      IF v_winner_id IS NULL THEN
        RAISE EXCEPTION 'Фракция-победитель «%» не найдена в базе.', p_winner_name;
      END IF;
      IF v_loser_id IS NULL THEN
        RAISE EXCEPTION 'Фракция-проигравший «%» не найдена в базе.', p_loser_name;
      END IF;

      v_fund := v_proposal.fund; -- ставка каждой стороны

      -- Победитель: +fund
      UPDATE public.factions SET sc_balance = sc_balance + v_fund WHERE id = v_winner_id;

      SELECT * INTO v_faction_row FROM public.factions WHERE id = v_winner_id;
      INSERT INTO public.balance_history (fraction_name, action_type, amount_change, description)
      VALUES (v_faction_row.name, v_proposal.type, v_fund,
              'Победа над ' || p_loser_name || '. ' || COALESCE(p_comment, ''));

      -- Проигравший: -fund
      UPDATE public.factions SET sc_balance = sc_balance - v_fund WHERE id = v_loser_id;

      SELECT * INTO v_faction_row FROM public.factions WHERE id = v_loser_id;
      INSERT INTO public.balance_history (fraction_name, action_type, amount_change, description)
      VALUES (v_faction_row.name, v_proposal.type, -v_fund,
              'Поражение от ' || p_winner_name || '. ' || COALESCE(p_comment, ''));

    -- ── Задание ──────────────────────────────────────────────
    ELSIF v_proposal.type = 'Задание' THEN
      -- Используем сумму, введённую Маратом вручную (или fund из заявки, если не указана)
      v_fund := COALESCE(p_task_reward, v_proposal.fund);

      UPDATE public.factions SET sc_balance = sc_balance + v_fund WHERE id = v_proposal.faction_id;

      SELECT * INTO v_faction_row FROM public.factions WHERE id = v_proposal.faction_id;
      INSERT INTO public.balance_history (fraction_name, action_type, amount_change, description)
      VALUES (v_faction_row.name, 'Задание', v_fund,
              'Выполнение гос. задания: ' || COALESCE(v_proposal.details, '') || '. ' || COALESCE(p_comment, ''));

      -- Обновляем fund в самой заявке для отчётности
      UPDATE public.sc_proposals SET fund = v_fund WHERE id = p_proposal_id;

    -- ── Викторина ─────────────────────────────────────────────
    ELSIF v_proposal.type = 'Викторина' THEN
      v_fund := v_proposal.fund;

      UPDATE public.factions SET sc_balance = sc_balance + v_fund WHERE id = v_proposal.faction_id;

      SELECT * INTO v_faction_row FROM public.factions WHERE id = v_proposal.faction_id;
      INSERT INTO public.balance_history (fraction_name, action_type, amount_change, description)
      VALUES (v_faction_row.name, 'Викторина', v_fund,
              'Правильный ответ на вопрос дня. ' || COALESCE(p_comment, ''));

    END IF;
  END IF;

  -- 5. Обновляем статус заявки
  UPDATE public.sc_proposals
  SET status        = p_new_status,
      admin_comment = p_comment,
      checked_by    = p_admin_login
  WHERE id = p_proposal_id;

  RETURN true;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

GRANT EXECUTE ON FUNCTION public.update_proposal_status_secure(bigint, text, text, text, text, integer, text, text) TO anon;


