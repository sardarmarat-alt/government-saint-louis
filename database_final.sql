-- ==============================================================
CREATE OR REPLACE FUNCTION public.set_new_password(
  p_login             text,
  p_old_password_hash text,
  p_new_password_hash text
)
RETURNS boolean AS $$
BEGIN
  IF EXISTS (
    SELECT 1 FROM public.admin_users
    WHERE username = p_login AND password = p_old_password_hash
  ) THEN
    UPDATE public.admin_users
    SET password = p_new_password_hash
    WHERE username = p_login;
    RETURN true;
  ELSE
    RETURN false;
  END IF;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

