CREATE OR REPLACE FUNCTION public.check_user_ban_status()
RETURNS TRIGGER AS $$
BEGIN
  -- Проверка бана по IP
  IF EXISTS (SELECT 1 FROM public.banned_ips WHERE ip = NEW.user_ip) THEN
    RAISE EXCEPTION 'Доступ ограничен: данный IP-адрес заблокирован администрацией.'
    USING ERRCODE = '42501';
  END IF;

  -- Антиспам: не чаще одного заявления в 10 минут с одного IP или под одним ником
  IF EXISTS (
    SELECT 1 FROM public.lawyer_statements
    WHERE (user_ip = NEW.user_ip OR LOWER(char_name) = LOWER(NEW.char_name))
      AND created_at > NOW() - INTERVAL '10 minutes'
  ) THEN
    RAISE EXCEPTION 'Слишком много запросов! Вы можете отправлять заявление не чаще, чем раз в 10 минут.'
    USING ERRCODE = '42P10';
  END IF;

  RETURN NEW;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

DROP TRIGGER IF EXISTS trigger_check_ban ON public.lawyer_statements;
CREATE TRIGGER trigger_check_ban
BEFORE INSERT ON public.lawyer_statements
FOR EACH ROW EXECUTE FUNCTION public.check_user_ban_status();


