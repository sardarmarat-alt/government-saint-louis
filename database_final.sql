-- Заблокированные IP-адреса
CREATE TABLE IF NOT EXISTS public.banned_ips (
  id         bigint generated by default as identity primary key,
  ip         text not null unique,
  created_at timestamptz default now()
);

-- Лог действий администраторов
CREATE TABLE IF NOT EXISTS public.admin_logs (
  id            bigint generated by default as identity primary key,
  created_at    timestamptz default now(),
  admin_login   text,
  statement_id  bigint,
  action_type   text,
  admin_comment text,
  admin_ip      text
);

-- Фракции (с балансом State Coins)
CREATE TABLE IF NOT EXISTS public.factions (
  id         bigserial primary key,
  name       text   not null unique,
  sc_balance bigint not null default 100000
);

-- Лидеры фракций (для авторизации на сайте)
CREATE TABLE IF NOT EXISTS public.leader_users (
  id         bigserial primary key,
  username   text   not null unique,
  password   text   not null,
  faction_id bigint references public.factions(id) on delete set null,
  created_at timestamptz default now()
);

-- Заявки и отчёты лидеров (SC-предложения)
CREATE TABLE IF NOT EXISTS public.sc_proposals (
  id                 bigserial primary key,
  created_at         timestamptz default now(),
  sender_login       text        not null,
  faction_id         bigint      references public.factions(id),
  type               text        not null, -- 'МФ_Бой', 'МФ_Тренировка', 'Задание', 'Викторина'
  details            text        not null,
  fund               bigint      not null default 0,
  winner_faction_id  bigint      references public.factions(id),
  with_faction_id    bigint      references public.factions(id),
  evidence_link      text        not null,
  status             text        not null default 'На рассмотрении',
  admin_comment      text,
  checked_by         text
);

-- Вопросы дня (интеллектуальная викторина)
CREATE TABLE IF NOT EXISTS public.daily_questions (
  id                bigserial primary key,
  created_at        timestamptz default now(),
  question_text     text        not null,
  reward            bigint      not null default 2500,
  is_active         boolean     not null default true,
  winner_faction_id bigint      references public.factions(id)
);

-- История изменений балансов
CREATE TABLE IF NOT EXISTS public.balance_history (
  id            bigserial primary key,
  created_at    timestamptz default now(),
  fraction_name text        not null,
  action_type   text        not null,
  amount_change bigint      not null, -- положительное или отрицательное
  description   text
);


